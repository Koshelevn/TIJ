package operators;


import java.lang.*;

public class MinMax {
    public static void main(String[] args) {
        float min_f = java.lang.Float.MIN_VALUE;
        float max_f = java.lang.Float.MAX_VALUE;
        double min_d = java.lang.Double.MIN_VALUE;
        double max_d = java.lang.Double.MAX_VALUE;
        System.out.println("min float :" + min_f);
        System.out.println("max float :" + max_f);
        System.out.println("min double :" + min_d);
        System.out.println("max double :" + max_d);
    }
}
